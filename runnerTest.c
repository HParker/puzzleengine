#include <check.h>
#include <stdio.h>
#include "puzzleData.h"

void test_solution(Runtime * rt, int moveCount, int moves[]) {
  for (int i = 0; i < moveCount; i++) {
    if (rt->gameWon == 0) {
      update(rt, moves[i]);
    }
  }
  if (rt->gameWon == 0) {
    printf("Game incomplete %i/%i\n", rt->levelIndex, rt->pd->levelCount);
  }
}

